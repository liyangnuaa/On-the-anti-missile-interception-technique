function y=fx(x)

V0=500;
s0=5000;
S=0.0606987;
m=400;
L=7.2;
D=0.278;
Jz=3456;
R=6371000;
Vs=20.046*sqrt(288.34-5.86*1e-3*x(2)*s0);     %% sound speed
g=9.806*(1-2*x(2)*s0/R);
rou=1.225*(1-2.0323*1e-5*x(2)*s0)^4.83;
alpha=x(6)-x(4);
ommegaz_=x(5)*D/(V0*x(3));

if x(3)*V0/Vs<0.6
    Cx0=0.14942;
    Cxa2=0.00186;
    Cya=0.06977;
    mzoz=-13.0898;
    mza=-0.1141;
elseif x(3)*V0/Vs<0.8
    k1=(0.8-x(3)*V0/Vs)/(0.8-0.6);
    k2=(x(3)*V0/Vs-0.6)/(0.8-0.6);
    Cx0=0.14942*k1+0.15754*k2;
    Cxa2=0.00186*k1+0.00190*k2;
    Cya=0.06977*k1+0.06948*k2;
    mzoz=-13.0898*k1-12.9252*k2;
    mza=-0.1141*k1-0.1131*k2;
elseif x(3)*V0/Vs<0.9
    k1=(0.9-x(3)*V0/Vs)/(0.9-0.8);
    k2=(x(3)*V0/Vs-0.8)/(0.9-0.8);
    Cx0=0.15754*k1+0.17140*k2;
    Cxa2=0.00190*k1+0.00192*k2;
    Cya=0.06948*k1+0.06933*k2;
    mzoz=-12.9252*k1-13.8327*k2;
    mza=-0.1131*k1-0.1102*k2;
elseif x(3)*V0/Vs<1.0
    k1=(1-x(3)*V0/Vs)/(1-0.9);
    k2=(x(3)*V0/Vs-0.9)/(1-0.9);
    Cx0=0.17140*k1+0.28832*k2;
    Cxa2=0.00192*k1+0.00200*k2;
    Cya=0.06933*k1+0.07114*k2;
    mzoz=-13.8327*k1-14.5677*k2;
    mza=-0.1102*k1-0.1225*k2;
elseif x(3)*V0/Vs<1.1
    k1=(1.1-x(3)*V0/Vs)/(1.1-1);
    k2=(x(3)*V0/Vs-1)/(1.1-1);
    Cx0=0.28832*k1+0.34312*k2;
    Cxa2=0.00200*k1+0.00209*k2;
    Cya=0.07114*k1+0.07427*k2;
    mzoz=-14.5677*k1-15.3393*k2;
    mza=-0.1225*k1-0.1303*k2;
else
    Cx0=0.34312;
    Cxa2=0.00209;
    Cya=0.07427;
    mzoz=-15.3393;
    mza=-0.1303;
end

cx=Cx0+Cxa2*alpha^2;
cy=Cya*alpha;
X=cx*0.5*rou*V0^2*x(3)^2*S;
Y=cy*0.5*rou*V0^2*x(3)^2*S;
Mz=(mza*alpha+mzoz*ommegaz_)*0.5*rou*V0^2*x(3)^2*S*L;

y=zeros(size(x));
y(1)=V0/s0*x(3)*cos(x(4));
y(2)=V0/s0*x(3)*sin(x(4));
y(3)=-(X+m*g*sin(x(4)))/(m*V0);
y(4)=(Y-m*g*cos(x(4)))/(m*V0*x(3));
y(5)=Mz/Jz;
y(6)=x(5);

